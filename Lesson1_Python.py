while num > 0:
    digit = num % 10
    if digit > greatest_dig:
        greatest_dig = digit
        if greatest_dig == 9:
            break
    num = num // 10 #num_init //=10
print(f"Наибольшая цифра в числе {num_init} равна {greatest_dig}")

